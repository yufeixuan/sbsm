package com.snsprj.sbsm.common;

public enum ResponseCode {

    SUCCESS(200,"success");

    private int code;
    private String msg;

    ResponseCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
